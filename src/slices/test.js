//Slice
const testSlice = createSlice({
  name: "test",
  initialState,
  reducers: {
    getTest: (state) => {
      state.loading = true;
    },
    getTestSuccess: (state, { payload }) => {
      state = { ...state, [payload.id]: payload };
      state.loading = false;
      state.hasErrors = false;
    },
    getTestFailure: (state) => {
      state.loading = false;
      state.hasErrors = true;
    },
    setSelectedAnswer: (state, action) => {
      const { questionID, answerIndex, testID } = action.payload;
      state[testID].questions[questionID].selected = answerIndex;
    },
  },
});

//Actions
export const {
  getTest,
  getTestSuccess,
  getTestFailure,
  setSelectedAnswer,
} = testSlice.actions;

//Selectors
export const testSelector = (state, testID) => state.test[testID];

export const questionSelector = (state, testID, questionID) => {
  return state.test[testID].questions[questionID];
};

//Reducer
export default testSlice.reducer;

//Functions
export const fetchTest = (id) => {
  return async (dispatch) => {
    dispatch(getTest());
    try {
      const response = await axios.get(
        `https://app.alumnica.org/api/odas/${id}/microodas/`
      );

      dispatch(getTestSuccess(response.data));
    } catch (error) {
      console.log(error);
      dispatch(getTestFailure());
    }
  };
};
